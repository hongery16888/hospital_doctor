package iori.hdoctor.net.response;

public class DoctorBenchResponse {

	private String uid;
	private String vnum;
	private String hnum;
	private String nolooknum;

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getVnum() {
		return vnum;
	}

	public void setVnum(String vnum) {
		this.vnum = vnum;
	}

	public String getHnum() {
		return hnum;
	}

	public void setHnum(String hnum) {
		this.hnum = hnum;
	}

	public String getNolooknum() {
		return nolooknum;
	}

	public void setNolooknum(String nolooknum) {
		this.nolooknum = nolooknum;
	}
}
